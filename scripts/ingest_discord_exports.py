def check_exists(title: str, section: str) -> bool:
    """Check if document already exists to avoid duplicates."""
    result = supabase.table("knowledge_documents").select("id").eq("title", title).eq("section", section).execute()
    return len(result.data) > 0


def insert_doc(title: str, content: str, section: str, topic: str, source: str = "discord-export"):
    """Insert a knowledge document into Supabase."""
    if check_exists(title, section):
        print(f"  ⏭  Skip (exists): {title}")
        return False

    doc = {
        "title": title,
        "content": content,
        "section": section,
        "topic": topic,
        "source": source,
        "metadata": {
            "ingested_at": datetime.now().isoformat(),
            "source_file": f"trading-theory/{source}.txt"
        }
    }
    supabase.table("knowledge_documents").insert(doc).execute()
    print(f"  ✅ Inserted: {title}")
    return True


